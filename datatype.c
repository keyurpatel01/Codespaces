/*
Datatype : type of data
int = whole numbers
float = decimal numbers
double = large decimal
char = single character
void = no value
*/

#include<stdio.h>
int main(){
    int age = 20;
    float height = 4.3;
    char grade = 'A';
    double salary = 83333.33333;
    printf("\n age is %d",age);//int
    printf("\n height is %f",height);//float
    printf("\n grade is %c",grade);//character
    printf("\n salary is %lf",salary);//double
    return 0;
}