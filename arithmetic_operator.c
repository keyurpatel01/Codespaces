/*
Arithmetic Operator : used for calculation
+,-,*,/,%

12345 % 10 to get last number.
*/
#include<stdio.h>
int main(){
    int num_1 = 10, num_2 = 20;
    printf("\n Num is %d",num_1+num_2);
    printf("\n Num is %d",num_1-num_2);
    printf("\n Num is %d",num_1*num_2);
    printf("\n Num is %d",num_1/num_2);
    printf("\n Num is %d",num_1%num_2);


}